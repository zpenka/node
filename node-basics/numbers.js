console.log('age plus 6 equals ' + results);

results = age - 1;
console.log('age minus 1 equals ' + results);

results = age * 3;
console.log('age times 3 equals ' + results);

results = age / 6;
console.log('age divided by 6 equals ' + results);

results = age % 5;
console.log('the remainder of age divided by 5 is ' + results);

results = results - 1;
console.log('minus one equals ' + results);

results = results++;
